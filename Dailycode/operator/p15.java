class bitwise{
	public static void main(String[] args){
		int x = 10;
		int y =15;
		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(x^y);
		System.out.println(~y);
		System.out.println(~x);
		System.out.println(x<<3);
		System.out.println(y>>2);
		System.out.println(y>>>3);
	}
}
